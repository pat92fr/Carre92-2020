/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_UART4_Init();
  MX_TIM12_Init();
  MX_TIM11_Init();
  MX_TIM10_Init();
  MX_TIM8_Init();
  MX_TIM7_Init();
  MX_TIM5_Init();
  MX_TIM3_Init();
  MX_TIM2_Init();
  MX_TIM1_Init();
  MX_UART5_Init();
  MX_SPI2_Init();
  MX_RTC_Init();
  MX_TIM14_Init();
  MX_TIM13_Init();
  /* USER CODE BEGIN 2 */

  // Initialise serial exchanges
  HAL_Serial_Init(&huart2, &hserial2);

  // Initialise robot
  Robot_Init();
  Robot_Start();
  LedSwitch(3,GPIO_PIN_SET);
  LedSwitch(4,GPIO_PIN_SET);

  // initialise TIM13 for 50Hz IT
  HAL_TIM_Base_Start_IT(&htim13);

  // initialise UART2 for command reception
  Message_StartReceive(5);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // User button pushed ?
    if(HAL_GPIO_ReadPin(USR_BUTT_GPIO_Port,USR_BUTT_Pin)==GPIO_PIN_RESET)
    {
      while(HAL_GPIO_ReadPin(USR_BUTT_GPIO_Port,USR_BUTT_Pin)==GPIO_PIN_RESET)
      {
        // wait until button release
      }
      if(robot.robot_on == 1)
      {
        LedSwitch(4,GPIO_PIN_SET);
        robot.robot_on=0;
        Robot_Stop();
      }
      else
      {
        LedSwitch(4,GPIO_PIN_RESET);
        robot.robot_on=1;
        Robot_Start();
      }
    }

    // read and process commands
    Robot_ReadCommand();

    // measure TIM13 IT duration
    if(robot.tim13_nb>=50)
    {
      int i;
      int tim13_ctr_max;
      float tim13_ctr_mean;

      tim13_ctr_max=0;
      tim13_ctr_mean=0;
      for(i=0;i<robot.tim13_nb;i++)
      {
        if( tim13_ctr_max < robot.tim13_ctr[i])
          tim13_ctr_max= robot.tim13_ctr[i];
        tim13_ctr_mean+=(float)robot.tim13_ctr[i];
      }
      tim13_ctr_mean/=(float)robot.tim13_nb;
      robot.tim13_nb=0;

      // output useful information
      char buf[32];
      Message_Send((uint8_t *)buf, sprintf(buf, "** TIM13 ctr max=%d mean=%f\r\n", tim13_ctr_max, tim13_ctr_mean));
    }
    HAL_Delay(100);
  }
  /* USER CODE END 3 */
}

